package Java.Common;

import java.util.*;
public class Common {
    public void printIntArray(int[] A) {
        for(int i : A) System.out.print(i + " ");
        System.out.println();
    }

    public void printCharArray(char[] A) {
        for(char i : A) System.out.print(i + " ");
        System.out.println();
    }
}
